from turtle import Turtle, Screen

timmy_the_turtle = Turtle()
timmy_the_turtle.shape("turtle") # Shape such as "Polygon", "Rectangle", "Circle", By Default its is NULL.
timmy_the_turtle.color("#ff0000"); # Color in HexCode
# timmy_the_turtle.color("red"); # Color

timmy_the_turtle.forward(100) # Turtle will move in forward direction.
timmy_the_turtle.backward(100) # Turtle will move in backwad direction.

timmy_the_turtle.left(90) # Turtle will move in left direction. (unit: angle i.e., degree)
timmy_the_turtle.forward(100) # Turtle will move in forward direction.

timmy_the_turtle.right(90) # Turtle will move in right direction. (unit: angle i.e., degree)
timmy_the_turtle.forward(100) # Turtle will move in forward direction.







screen = Screen()
screen.exitonclick()
